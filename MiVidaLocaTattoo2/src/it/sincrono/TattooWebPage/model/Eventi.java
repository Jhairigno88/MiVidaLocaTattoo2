package it.sincrono.TattooWebPage.model;

import java.util.Date;

public class Eventi {
	
	private String nomeE, luogoE;
	private Date dataE;
	
	public Eventi() {
		
	}

	public String getNomeE() {
		return nomeE;
	}

	public void setNomeE(String nomeE) {
		this.nomeE = nomeE;
	}

	public String getLuogoE() {
		return luogoE;
	}

	public void setLuogoE(String luogoE) {
		this.luogoE = luogoE;
	}

	public Date getDataE() {
		return dataE;
	}

	public void setDataE(Date dataE) {
		this.dataE = dataE;
	}

	
	}
	

